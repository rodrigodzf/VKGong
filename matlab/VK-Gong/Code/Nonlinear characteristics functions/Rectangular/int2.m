function [ y ] = int2(m,p,L)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%                                                                   %
%                                                                   %
%                              VK-Gong                              % 
%                                                                   %
%                                                                   %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%Integral of X(m,x)*X(p,x) from zero to L, where X is the clamped plate
%function and d denotes derivative in x (dd = double derivative)

if m == 0 && p == 0
    
    y = (10*L)/7;
    
        elseif m == p 
            
                y = (67*L)/70 - ((-1)^m*L)/35 - (768*L)/(pi^4*m^4) - (672*(-1)^m*L)/(pi^4*m^4);
    
        elseif m == 0 
        
                y = (3*L*((-1)^p + 1)*(pi^4*p^4 - 1680))/(14*pi^4*p^4);

        
        elseif p == 0
        
                y = (3*L*((-1)^m + 1)*(pi^4*m^4 - 1680))/(14*pi^4*m^4);
        
else
    
    y = - (L*(11760*(-1)^m + 11760*(-1)^p - 16*pi^4*m^4 + 13440*(-1)^m*(-1)^p + (-1)^m*pi^4*m^4 + (-1)^p*pi^4*m^4 - 16*(-1)^m*(-1)^p*pi^4*m^4 + 13440))/(70*pi^4*m^4) - (L*(13440*m^4 + 11760*(-1)^m*m^4 + 11760*(-1)^p*m^4 + 13440*(-1)^m*(-1)^p*m^4))/(70*pi^4*m^4*p^4);
    
end

end
        
    





